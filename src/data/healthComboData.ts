export function getHealthComboText(
  dayStem: string,
  gyeokguk: string,
  strengthType: string,
  strongElement: string,
  weakElement: string
): string {
  const key = `${dayStem}-${gyeokguk}-${strengthType}`;

  const db: Record<string, string> = {
    "갑-편재격-신강": `
갑목 일간이 편재격이고 신강한 구조입니다.
에너지가 강하고 활동량이 많아 과로, 근육 긴장, 간 피로가 누적되기 쉽습니다.
일을 크게 벌릴수록 수면과 휴식 리듬을 반드시 관리해야 합니다.
`,
    "신-정인격-신강": `
신금 일간이 정인격이고 신강한 구조입니다.
예민한 감각과 분석력이 강해 신경 피로, 호흡기, 피부 건조에 주의가 필요합니다.
생각이 많아질수록 몸이 긴장되므로 휴식과 수면 관리가 중요합니다.
`,
  };

  const dayHealthMap: Record<string, string> = {
    갑: "갑목 일간은 간, 담, 근육, 눈의 피로와 연결해서 보는 것이 좋습니다.",
    을: "을목 일간은 신경성 피로, 혈액순환, 목·어깨 긴장에 주의가 필요합니다.",
    병: "병화 일간은 심장, 혈압, 눈 피로, 열감 관리가 중요합니다.",
    정: "정화 일간은 수면, 혈액순환, 눈 피로, 심리적 예민함을 함께 봐야 합니다.",
    무: "무토 일간은 위장, 소화기, 체중, 순환 정체에 주의가 필요합니다.",
    기: "기토 일간은 소화기, 피부, 습기, 만성 피로 관리가 중요합니다.",
    경: "경금 일간은 폐, 호흡기, 피부, 근육 긴장, 사고성 부상에 주의가 필요합니다.",
    신: "신금 일간은 호흡기, 피부, 신경 피로, 예민성 관리가 중요합니다.",
    임: "임수 일간은 신장, 방광, 하체 냉증, 수면 리듬을 관리해야 합니다.",
    계: "계수 일간은 신장, 혈액순환, 냉증, 불안감, 수면 질을 함께 봐야 합니다.",
  };

  const geokgukHealthMap: Record<string, string> = {
    정관격: "책임과 긴장을 오래 유지하는 구조이므로 어깨, 목, 소화, 수면 리듬 관리가 중요합니다.",
    편관격: "압박과 경쟁 속에서 에너지를 많이 쓰므로 과로, 사고, 혈압, 근육 긴장을 조심해야 합니다.",
    정재격: "현실 책임과 생활 관리가 강하게 작용하므로 소화기, 체중, 만성 피로 관리가 필요합니다.",
    편재격: "활동량과 대인관계가 많아지기 쉬우므로 간 피로, 수면 부족, 과음·과식 관리가 중요합니다.",
    식신격: "식습관은 좋지만 몸이 무거워질 수 있으므로 식습관, 체중, 소화 관리를 꾸준히 해야 합니다.",
    상관격: "말, 생각, 감정 표현이 많아 신경 피로와 수면 질이 흔들릴 수 있습니다.",
    정인격: "생각이 많고 몸을 덜 움직이기 쉬우므로 순환, 체력, 소화 기능을 의식적으로 관리해야 합니다.",
    편인격: "예민함과 집중력이 강해 수면, 신경 안정, 불안감, 소화 기능을 함께 살피는 것이 좋습니다.",
    비견격: "기본 체력은 좋은 편이지만 회복을 미루는 습관이 누적되면 후반기에 컨디션 저하로 이어질 수 있습니다.",
    겁재격: "경쟁과 대인관계에서 에너지가 많이 소모되므로 스트레스, 간 피로, 수면 부족을 조심해야 합니다.",
  };

  const strengthHealthText =
    strengthType === "신강"
      ? "신강 구조에서는 기본 에너지는 강하지만, 무리하게 버티다가 피로가 누적될 수 있습니다. 쉬는 것도 운을 지키는 방법입니다."
      : strengthType === "신약"
      ? "신약 구조에서는 체력 소모가 빠르게 느껴질 수 있으므로 일정, 수면, 식사, 회복 시간을 먼저 확보해야 합니다."
      : "중화 구조에서는 큰 무리만 피하면 비교적 균형을 유지하기 쉽습니다. 다만 생활 리듬이 깨지면 건강운도 흔들릴 수 있습니다.";

  return `
🏥 건강운 조합 분석

${db[key] || ""}

■ 일간 기준 건강 포인트
${dayHealthMap[dayStem] || "타고난 체질과 생활 패턴에 맞는 건강 관리 전략이 중요합니다."}

■ 격국 기준 건강 포인트
${geokgukHealthMap[gyeokguk] || "격국의 흐름을 기준으로 생활 리듬과 스트레스 관리가 중요합니다."}

■ 오행 균형 포인트
${strongElement} 기운은 현재 체력과 활동 에너지의 강점으로 활용하고,
${weakElement} 기운은 수면·식사·운동·회복 루틴을 통해 균형을 맞추는 것이 좋습니다.

■ 신강·신약 기준 건강 조언
${strengthHealthText}

※ 건강운은 의학적 진단이 아니라 사주 오행 균형을 참고한 생활관리 조언입니다.
【철학관 건강 상담】

이 사주는 작은 피로가 반복되면서 컨디션 저하로 이어지는 경향이 있습니다.

${strongElement} 기운은 회복력을 높이는 장점으로 활용하고,
${weakElement} 기운과 관련된 생활 습관을 꾸준히 관리하는 것이 건강운을 안정시키는 핵심입니다.
`;
}
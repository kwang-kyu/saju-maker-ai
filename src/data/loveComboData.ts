    기: "상대를 챙기고 맞춰주는 마음이 강합니다. 다만 지나치게 맞춰주다 보면 본인의 감정이 뒤로 밀릴 수 있습니다.",
    경: "관계에서도 분명한 약속과 기준을 중요하게 봅니다. 애매한 관계보다 확실한 신뢰를 원합니다.",
    신: "기준이 높고 쉽게 흔들리지 않는 편입니다. 하지만 마음에 드는 사람에게는 깊고 오래가는 애정을 보입니다.",

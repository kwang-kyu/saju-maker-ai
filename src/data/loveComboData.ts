export function getLoveComboText(
  dayStem: string,
  gyeokguk: string,
  strengthType: string,
  strongElement: string,
  weakElement: string
): string {
  const relationshipStyle =
    strengthType === "신강"
      ? "관계에서 본인의 기준과 주도성이 강하게 나타나는 편입니다. 그래서 좋은 인연을 만나도 상대를 내 방식에 맞추려 하면 관계가 답답해질 수 있습니다."
      : strengthType === "신약"
      ? "관계에서 상대의 기분과 흐름을 많이 살피는 편입니다. 좋은 배려가 장점이지만, 지나치게 맞추다 보면 본인의 감정이 쌓일 수 있습니다."
      : "관계에서 감정과 현실을 함께 보려는 균형감이 있습니다. 다만 상황에 따라 마음이 흔들릴 수 있으므로 기준을 분명히 세우는 것이 좋습니다.";

  return `
❤️ 관계 현실 상담
타고난 관계 성향은 ${dayStem}의 기본 기질을 참고하되, 실제 상담에서는 생활 방식과 신뢰의 흐름을 더 중요하게 봅니다.
💡 사랑에서 가장 먼저 봐야 할 점
연애운은 단순히 인연이 많고 적음으로만 보지 않습니다.
중요한 것은 어떤 사람에게 끌리고, 어떤 관계에서 오래 안정되는가입니다.

💡 관계에서 반복되기 쉬운 흐름
${relationshipStyle}

💡 좋은 인연이 들어오는 방식
${gyeokguk} 흐름에서는 감정의 끌림만큼 생활 방식, 돈 관리, 책임감, 대화 방식이 중요하게 작용합니다.
처음부터 강하게 끌리는 사람보다 시간이 지날수록 편안함과 신뢰를 주는 사람이 더 오래 남을 수 있습니다.

💡 관계에서 조심할 부분
강하게 드러나는 ${strongElement} 흐름은 매력과 존재감으로 나타날 수 있지만, 과하면 상대에게 부담으로 느껴질 수 있습니다.
반대로 부족한 ${weakElement} 흐름은 대화, 배려, 현실 조율에서 의식적으로 보완하는 것이 좋습니다.

👴 철학관 원장의 한마디
좋은 인연은 나를 흔드는 사람이 아니라, 내 삶을 안정시키는 사람입니다.
궁합은 상대 사주까지 함께 보아야 정확하지만, 본인 기준으로는 설렘보다 신뢰, 감정보다 생활 태도, 말보다 행동을 보는 것이 중요합니다.
`;
}


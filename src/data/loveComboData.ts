    정관: "책임감 있고 약속을 잘 지키는 사람과 잘 맞습니다.",
    편관: "추진력 있고 강한 에너지를 가진 사람에게 끌릴 수 있지만, 상대가 지나치게 강하면 갈등도 커질 수 있습니다.",

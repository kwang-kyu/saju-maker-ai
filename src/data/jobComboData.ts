export function getCareerPersonaText(
  dayStem: string,
  gyeokguk: string,
  strengthType: string,
  strongestElement: string,
  weakestElement: string
): string {
  const combined = `${dayStem} ${gyeokguk} ${strengthType} ${strongestElement} ${weakestElement}`;

  if (
    combined.includes("재") ||
    combined.includes("토") ||
    combined.includes("금")
  ) {
    return `
    ■ 직업 페르소나
부동산 중개·개발형 사주입니다.
입지·정보·계약 흐름을 읽는 능력이 있습니다.
중개, 개발기획, 자산 컨설팅과 인연이 깊습니다.
    `;
  }

  if (
    combined.includes("식") ||
    combined.includes("상") ||
    combined.includes("화")
  ) {
    return `
   ■ 직업 페르소나
창업·브랜드형 사주입니다.
기획, 브랜딩, 마케팅 감각이 좋습니다.
작게 시작해 시스템을 만드는 방식이 유리합니다.
    `;
  }

  if (
    combined.includes("인") ||
    combined.includes("수") ||
    combined.includes("목")
  ) {
    return `
   ■ 직업 페르소나
컨설턴트·교육·연구형 사주입니다.
상담, 교육, 연구, 컨설팅과 인연이 깊습니다.
경험과 지식이 쌓일수록 가치가 커집니다.
    `;
  }

  return `
  ■ 직업 페르소나
조직 리더·운영형 사주입니다.
관리, 기획, 운영 능력이 안정적으로 발휘됩니다.
나이가 들수록 총괄 운영 역할에서 강점이 나타납니다.
  `;

}

export function getCareerAgeText(birthYear: number): string {
  const age = new Date().getFullYear() - birthYear + 1;

  if (age < 40) {
    return `
■ 인생 단계
현재는 기반을 넓히고 새로운 기회를 개척하는 시기입니다.
경험과 사람을 최대한 많이 쌓는 것이 중요합니다.
`;
  }

  if (age < 60) {
    return `
■ 인생 단계
현재는 전문성과 경험을 수익 구조로 연결하는 확장기입니다.
축적된 역량을 브랜드와 자산으로 전환하는 전략이 중요합니다.
`;
  }

  return `
■ 인생 단계
현재는 성과를 크게 확장하기보다 핵심 자산과 경험을 정리하고
안정적인 현금 흐름과 후반기 삶의 균형을 만드는 시기입니다.
`;
}
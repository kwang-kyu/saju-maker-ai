import type { BasicSajuResult } from "../../types/basic";

function getAge(birthDate?: string) {
  const birthYear = Number(String(birthDate || "").slice(0, 4));
  if (!birthYear) return 0;
  return new Date().getFullYear() - birthYear + 1;
}

function getAgeCareerStrategy(age: number) {
  if (!age) return "현재는 나이보다 경력 수준, 수입 구조, 전문성 축적 상태를 기준으로 직업 방향을 보는 것이 좋습니다.";
  if (age <= 34) return "지금은 완벽한 직업을 찾기보다 실무 경험, 기술, 포트폴리오, 나에게 맞는 업무 방식을 찾는 것이 중요합니다.";
  if (age <= 49) return "지금은 단순히 열심히 일하는 것보다 전문성, 권한, 수입 구조, 장기 성장성을 함께 봐야 하는 시기입니다.";
  if (age <= 64) return "지금은 기존 경험을 관리, 교육, 자문, 영업, 컨설팅, 부업, 소규모 사업으로 바꾸는 전략이 중요합니다.";
  return "지금은 경쟁보다 경험, 신뢰, 관계를 활용하는 일이 좋습니다. 건강을 해치지 않으면서 보람과 생활 리듬을 지키는 방향이 맞습니다.";
}

function getRoleStrategy(data: BasicSajuResult) {
  const strong = String(data.strongestElement || "");

  if (strong.includes("목") || strong.includes("화")) {
    return "기획, 표현, 상담, 교육, 홍보, 브랜딩처럼 사람을 움직이고 방향을 제시하는 역할에서 강점이 살아납니다.";
  }

  if (strong.includes("토") || strong.includes("금")) {
    return "관리, 판단, 정리, 계약, 운영, 책임 있는 의사결정처럼 기준을 세우는 역할에서 강점이 살아납니다.";
  }

  if (strong.includes("수")) {
    return "정보 분석, 전략 수립, 흐름 파악, 기획 지원, 상담처럼 생각을 정리해 방향을 잡는 역할에서 강점이 살아납니다.";
  }

  return "단순 반복보다 판단, 상담, 정리, 기획, 고객 신뢰가 남는 역할에서 직업운이 안정됩니다.";
}

export function jobConsulting(data: BasicSajuResult, birthDate?: string): string {
  const name = data.name;
  const age = getAge(birthDate);
  const ageCareerStrategy = getAgeCareerStrategy(age);
  const roleStrategy = getRoleStrategy(data);

  return `
직업 원장 상담

1. 원장 판단

결론부터 말씀드리면,
${name}님은 직업 이름보다 역할과 환경을 먼저 봐야 합니다.

어떤 직업이 좋다, 나쁘다보다
내 능력이 인정되고,
경험이 쌓이고,
수입 구조가 오래 이어질 수 있는지가 더 중요합니다.

2. 현재 커리어 분석

${name}님은 아무 일이나 오래 버티는 방식보다
책임과 기준이 분명하고,
내가 판단할 수 있는 여지가 있는 환경에서 힘이 살아납니다.

역할이 애매하거나
일은 많은데 인정이 적거나
성과 기준이 불분명한 곳에서는 쉽게 지칠 수 있습니다.

3. 잘 맞는 역할

${roleStrategy}

직업을 고를 때는 직함보다 실제 업무를 봐야 합니다.

매일 하는 일이 무엇인지,
내가 결정할 수 있는 범위가 있는지,
성과가 기록으로 남는지,
경험이 쌓여 다음 기회로 이어지는지를 확인해야 합니다.

4. 피해야 할 직업 환경

다음 환경은 신중해야 합니다.

- 책임은 큰데 권한은 없는 곳
- 성과 기준이 불분명한 곳
- 사람 감정만 계속 받아내야 하는 곳
- 오래 일해도 기술이나 기록이 남지 않는 곳
- 수입 구조가 불안정한데 고정 부담이 큰 곳

직업운은 버티는 힘만으로 좋아지지 않습니다.
맞지 않는 환경을 오래 견디면 건강과 자신감까지 흔들릴 수 있습니다.

5. 이직과 전환 판단

이직은 감정으로 결정하면 안 됩니다.

다음 네 가지를 확인한 뒤 움직이는 것이 좋습니다.

첫째, 지금 일에서 더 배울 것이 남아 있는가.
둘째, 옮기려는 곳에서 역할과 권한이 더 분명한가.
셋째, 수입이 늘어도 체력과 시간 부담이 감당 가능한가.
넷째, 1년 뒤 내 경력에 남는 결과물이 생기는가.

이 네 가지 중 두 가지 이상이 좋아진다면 전환을 검토할 수 있습니다.
반대로 단순히 힘들어서 피하는 이직은 비슷한 문제가 반복될 수 있습니다.

6. 나이에 맞는 직업 전략

${ageCareerStrategy}

7. 앞으로 3년 커리어 로드맵

1년 차에는 현재 일에서 내가 잘하는 역할과 반복적으로 지치는 업무를 구분해야 합니다.

2년 차에는 남들이 보기에도
"이 분야는 ${name}님이 잘한다"
라고 말할 수 있는 결과물을 만들어야 합니다.

3년 차에는 검증된 강점을 바탕으로
직책,
수입,
부업,
프리랜서,
교육,
상담,
소규모 사업 중 하나로 확장할 수 있습니다.

8. 오늘부터 실천할 것

- 지금 하는 일 중 잘하는 업무 3가지를 적으십시오.
- 반복적으로 지치는 업무 3가지를 적으십시오.
- 내 경력에 남는 결과물을 하나 정하십시오.
- 이직을 고민한다면 연봉보다 역할, 권한, 성장성을 같이 비교하십시오.
- 앞으로 3년 동안 쌓을 전문 분야를 하나 정하십시오.

최종 총평

${name}님의 직업운은 이름 좋은 직업을 찾는 것보다
내 능력이 인정되는 환경을 찾는 것이 핵심입니다.

앞으로는 아무 일이나 버티기보다
경험이 쌓이고,
내 이름이 남고,
수입 구조가 이어지는 방향으로 커리어를 잡아야 합니다.

직업은 생계이기도 하지만
시간이 쌓였을 때 나를 설명해주는 이력이기도 합니다.
그 이력이 남는 방향으로 가면 직업운은 훨씬 안정적으로 살아납니다.
`.trim();
}
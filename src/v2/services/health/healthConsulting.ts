import type { BasicSajuResult } from "../../types/basic";

export function getHealthConsulting(data: BasicSajuResult): string {
  const name = data.name;

  const sajuHealthPoint =
    `${data.dayMaster} 일간, ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면 ${name}님의 건강운은 체력 자체보다 생활 리듬과 회복력의 균형에서 더 크게 갈립니다.`;

  const strongHealthPoint =
    `강하게 살아나는 ${data.strongestElement} 기운은 몸과 마음을 버티게 하는 기본 힘입니다. 이 기운이 잘 살아날 때 컨디션 회복도 빨라집니다.`;

  const weakHealthPoint =
    `부족한 ${data.weakestElement} 기운은 건강 관리에서 보완해야 할 지점입니다. 이 부분이 무너지면 피로가 쌓이거나 생활 리듬이 쉽게 흐트러질 수 있습니다.`;
  return `
제가 ${name}님의 건강운을 볼 때 먼저 말씀드리고 싶은 것은,
건강운은 단순히 병이 있다 없다로 보는 것이 아니라
몸이 어떤 방식으로 피로를 쌓고,
어떤 생활 리듬에서 무너지기 쉬운지를 보는 것이 중요하다는 점입니다.

${sajuHealthPoint}

${strongHealthPoint}

${weakHealthPoint}

🩺 원장님 말씀

${name}님은 몸이 한 번에 크게 무너지는 흐름보다는
작은 피로가 쌓이다가 어느 순간 컨디션이 떨어지는 흐름을 조심해야 합니다.

겉으로는 괜찮아 보여도
속으로는 긴장과 피로를 오래 붙잡고 있을 수 있습니다.

그래서 ${name}님 건강운의 핵심은
무리하지 않는 것보다
피로가 쌓이기 전에 풀어주는 습관입니다.

🌿 건강 성향

${name}님은 생활 리듬이 안정될 때 몸 상태도 좋아지는 편입니다.

잠자는 시간,
식사 시간,
움직이는 시간,
쉬는 시간이 불규칙해지면
몸이 쉽게 무거워질 수 있습니다.

특히 정신적으로 신경 쓸 일이 많아지면
몸보다 마음이 먼저 지치고,
그 영향이 소화, 수면, 집중력, 피로감으로 이어질 수 있습니다.

⚠️ 몸이 지치는 패턴

${name}님은 바쁠 때는 참고 버티다가
일이 끝난 뒤에 피로가 몰려오는 흐름이 생기기 쉽습니다.

당장은 괜찮다고 느껴도
몸은 이미 긴장 상태를 오래 유지하고 있을 수 있습니다.

이런 흐름이 반복되면
잠을 자도 개운하지 않거나,
몸이 무겁고,
작은 일에도 예민해질 수 있습니다.

🍚 생활습관에서 조심할 점

${name}님은 몸에 좋은 것을 많이 챙기는 것보다
나쁜 습관을 줄이는 것이 더 중요합니다.

늦은 식사,
불규칙한 수면,
과한 카페인,
오래 앉아 있는 습관,
스트레스를 참고 넘기는 습관은
건강운을 약하게 만들 수 있습니다.

특히 컨디션이 떨어질 때
억지로 버티는 습관은 줄이는 것이 좋습니다.

💤 수면과 회복

${name}님에게 수면은 단순한 휴식이 아니라
운의 흐름을 회복시키는 가장 중요한 기반입니다.

잠이 부족하면 판단력도 흐려지고,
감정 기복도 커지며,
일과 관계에서도 쉽게 지칠 수 있습니다.

가능하면 매일 같은 시간대에 잠들고 일어나는 흐름을 만드는 것이 좋습니다.

🧘 마음 건강

${name}님은 생각이 많아질수록 몸도 함께 굳어질 수 있습니다.

걱정이 길어지면 어깨, 목, 위장, 두통, 피로감처럼
몸으로 신호가 나타날 수 있습니다.

그래서 건강을 지키려면
운동만큼이나 마음을 비우는 시간이 필요합니다.

짧은 산책,
가벼운 스트레칭,
휴대폰을 내려놓는 시간,
혼자 조용히 쉬는 시간이 도움이 됩니다.

📌 앞으로 3년 건강 전략

앞으로 3년 동안 ${name}님의 건강운에서 중요한 것은
큰 변화보다 생활 리듬을 안정시키는 것입니다.

1년 차에는 무리한 목표보다
수면과 식사 시간을 먼저 정리하는 것이 좋습니다.

2년 차에는 체력 관리가 중요합니다.
가벼운 운동을 꾸준히 이어가야 몸의 기본 힘이 살아납니다.

3년 차에는 스트레스 관리가 핵심입니다.
일이 많아질수록 쉬는 시간을 의식적으로 확보해야 합니다.

✅ 실천 조언

첫째, 피곤할 때 더 버티지 말고 바로 쉬는 습관을 만드세요.

둘째, 식사와 수면 시간을 일정하게 유지하는 것이 좋습니다.

셋째, 무리한 운동보다 오래 할 수 있는 가벼운 운동이 더 잘 맞습니다.

넷째, 몸이 보내는 작은 신호를 무시하지 마세요.

다섯째, 스트레스를 혼자 오래 담아두지 않는 것이 중요합니다.

🌙 최종 총평

${name}님의 건강운은 큰 병을 겁내기보다
생활 리듬이 무너지면서 생기는 피로 누적을 조심해야 하는 흐름입니다.

몸은 갑자기 나빠지는 것이 아니라
매일의 작은 습관 속에서 천천히 신호를 보냅니다.

${name}님에게 가장 좋은 건강 관리는
무리한 관리가 아니라
규칙적인 생활,
충분한 회복,
마음을 쉬게 하는 시간입니다.

앞으로는 몸을 더 강하게 몰아붙이기보다
몸이 편안하게 회복될 수 있는 환경을 만들어가는 것이 중요합니다.
`;
}

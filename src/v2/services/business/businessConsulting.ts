import type { BasicSajuResult } from "../../types/basic";

function getAge(birthDate?: string) {
  const birthYear = Number(String(birthDate || "").slice(0, 4));
  if (!birthYear) return 0;
  return new Date().getFullYear() - birthYear + 1;
}

function getBusinessAgeStrategy(name: string, age: number) {
  if (!age) return `${name}님은 나이보다 현재 자금, 고객 확보 능력, 반복 매출 구조를 기준으로 사업을 판단하는 것이 좋습니다.`;

  if (age <= 34) return `${name}님은 처음부터 크게 벌리기보다 작은 상품, 작은 서비스, 온라인 테스트, 지인 고객 반응부터 확인하는 것이 좋습니다.`;

  if (age <= 49) return `${name}님은 혼자 열심히 하는 방식에서 벗어나 고객 관리, 외주, 광고, 재구매 구조를 시스템으로 만들어야 합니다.`;

  if (age <= 64) return `${name}님은 무리한 신규 확장보다 경험을 활용한 상담, 교육, 관리, 소개, 지역 기반 서비스처럼 오래 갈 수 있는 구조가 좋습니다.`;

  return `${name}님은 큰 확장보다 자산 보호, 경험 활용, 자문, 안정적인 현금흐름을 우선하는 것이 좋습니다.`;
}

function getBusinessRole(data: BasicSajuResult) {
  const strong = String(data.strongestElement || "");

  if (strong.includes("목") || strong.includes("화")) {
    return "대표는 기획, 상담, 브랜딩, 고객 설득을 맡고 반복 업무는 시스템이나 외주로 분리하는 것이 좋습니다.";
  }

  if (strong.includes("토") || strong.includes("금")) {
    return "대표는 기준 설정, 운영 관리, 계약, 고객 신뢰 관리에 강점을 두고 움직이는 것이 좋습니다.";
  }

  if (strong.includes("수")) {
    return "대표는 시장 흐름, 콘텐츠 방향, 고객 데이터, 전략 판단을 맡을 때 강점이 살아납니다.";
  }

  return "대표는 모든 일을 직접 하기보다 상담, 기획, 고객 신뢰 형성에 집중하는 것이 좋습니다.";
}


import type { BasicSajuResult } from "../../types/basic";
import { buildSajuIdentityProfile } from "../profile/sajuIdentityProfile";

function getAge(birthDate?: string) {
  const birthYear = Number(String(birthDate || "").slice(0, 4));
  if (!birthYear) return 0;
  return new Date().getFullYear() - birthYear + 1;
}

function getBusinessAgeStrategy(name: string, age: number) {
  if (!age) {
    return `${name}님의 사업운은 나이보다 현재 자본 규모, 경험, 고객 확보 능력, 반복 매출 구조를 기준으로 보는 것이 좋습니다. 무리한 확장보다 작게 검증하고 숫자로 판단하는 전략이 필요합니다.`;
  }

  if (age <= 34) {
    return `${name}님의 사업운은 20~34세 흐름에서 큰 창업보다 작은 검증이 중요합니다. 처음부터 매장, 인건비, 재고, 광고비를 크게 쓰기보다 작은 상품, 작은 서비스, 온라인 테스트, 지인 고객, 반복 구매 가능성을 먼저 확인해야 합니다. 이 시기에는 돈보다 경험과 고객 반응을 모으는 것이 사업운을 키우는 길입니다.`;
  }

  if (age <= 49) {
    return `${name}님의 사업운은 35~49세 흐름에서 확장과 시스템이 중요해집니다. 단순히 혼자 열심히 하는 방식만으로는 한계가 오기 쉽기 때문에 직원, 외주, 고객 관리, 광고, 재구매 구조를 정리해야 합니다. 이 시기에는 매출보다 순이익, 브랜드 신뢰, 운영 시스템을 만드는 것이 핵심입니다.`;
  }

  if (age <= 64) {
    return `${name}님의 사업운은 50~64세 흐름에서 안정 운영과 제2수입 구조가 중요합니다. 무리한 신규 확장보다 기존 경험을 활용해 컨설팅, 교육, 관리, 소개, 지역 기반 서비스처럼 오래 갈 수 있는 사업 구조가 좋습니다. 이 시기에는 체력과 시간을 과하게 쓰는 사업보다 경험이 수익이 되는 구조를 만들어야 합니다.`;
  }

  return `${name}님의 사업운은 65세 이후에는 큰 확장보다 자산 보호, 경험 활용, 자문, 승계, 안정적인 현금흐름이 중요합니다. 직접 모든 것을 운영하기보다 믿을 수 있는 사람에게 역할을 나누고, 본인은 판단과 관리 중심으로 움직이는 것이 좋습니다. 이 시기에는 욕심보다 안정이 사업운을 지켜줍니다.`;
}




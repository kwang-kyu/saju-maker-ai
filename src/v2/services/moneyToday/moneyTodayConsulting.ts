export function moneyTodayConsulting(name: string): string {
  const today = new Date().toLocaleDateString("ko-KR");

  return `
${today}, ${name}님의 오늘 재물 흐름은 크게 벌기보다 새는 돈을 막는 데 초점이 있습니다.

오늘은 돈이 들어오느냐보다 돈을 어떻게 쓰느냐가 더 중요합니다.

소비는 꼭 필요한 지출인지 먼저 확인하세요.
계약이나 결제는 금액, 기간, 조건을 다시 보는 것이 좋습니다.
투자는 공격적으로 움직이기보다 관망하는 편이 안정적입니다.
수입은 새로운 곳보다 기존 흐름 안에서 작은 기회가 생길 수 있습니다.

오늘 가장 조심할 것은 충동 지출입니다.
돈을 쓰기 전에 이 돈이 지금 꼭 필요한가를 한 번만 물어보세요.

최종적으로 ${name}님은 오늘 큰돈을 움직이기보다 지출 기준을 세우는 것이 좋습니다.
`.trim();
}

import type { BasicSajuResult } from "../../types/basic";

export function getMarriageConsulting(data: BasicSajuResult): string {
  const name = data.name;

  const sajuMarriagePoint =
    `${data.dayMaster} 일간, ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면 ${name}님의 결혼운은 빠른 결정 보다 신뢰, 생활 안정, 책임감의 균형에서 더 크게 갈립니다.`;

  const strongMarriagePoint =
    `강하게 살아나는 ${data.strongestElement} 기운은 결혼생활에서 자연스럽게 드러나는 장점입니다. 이 기운이 잘 살아날 때 관계를 유지하는 힘이 커집니다.`;

  const weakMarriagePoint =
    `부족한 ${data.weakestElement} 기운은 결혼에서 보완해야 할 지점입니다. 이 부분이 흔들리면 마음은 있어도 생활 리듬이나 대화에서 갈등이 생길 수 있습니다.`;
  return `
제가 ${name}님의 결혼운을 볼 때 먼저 말씀드리고 싶은 것은,
결혼운은 단순히 결혼을 하느냐 마느냐가 아니라
어떤 사람과 어떤 방식으로 살아갈 때 안정되는지를 보는 것이 중요하다는 점입니다.

${sajuMarriagePoint}

${strongMarriagePoint}

${weakMarriagePoint}

💍 원장님 말씀

${name}님은 결혼에서 감정보다 신뢰와 생활의 안정이 중요합니다.

처음의 설렘도 필요하지만,
오래 함께 살아갈 사람이라면 결국 책임감,
생활 태도,
돈을 대하는 방식,
대화 방식이 훨씬 중요하게 작용합니다.

💞 결혼관

${name}님은 결혼을 가볍게 결정하기보다
상대가 내 삶에 안정감을 줄 수 있는 사람인지 오래 살피는 편이 좋습니다.

마음이 끌린다고 해서 바로 결정하기보다는
생활 속에서 서로 맞춰갈 수 있는지를 보는 것이 중요합니다.

🏡 배우자 선택 기준

${name}님에게 좋은 배우자는
말이 화려한 사람보다 행동이 꾸준한 사람입니다.

감정적으로 흔들기보다
현실적인 문제를 함께 해결하려는 사람이 잘 맞습니다.

특히 약속,
경제관념,
가족에 대한 태도,
일상 습관을 꼭 보셔야 합니다.

⚠️ 결혼 후 갈등 패턴

${name}님은 서운함을 바로 말하지 않고 참다가
마음속에 쌓아두는 흐름이 생길 수 있습니다.

결혼 후에는 작은 생활 습관 차이가
큰 감정 문제로 번질 수 있기 때문에
초반부터 대화하는 습관이 중요합니다.

🌿 결혼 후 좋아지는 흐름

서로의 역할이 분명하고,
경제적인 기준이 맞으며,
감정을 차분히 나눌 수 있는 관계라면
${name}님의 결혼운은 안정적으로 좋아집니다.

결혼은 ${name}님에게 부담이 아니라
삶의 중심을 잡아주는 기반이 될 수 있습니다.

📌 앞으로 3년 결혼 전략

1년 차에는 사람을 만날 때 감정보다 생활 태도를 보셔야 합니다.

2년 차에는 관계가 깊어진다면
돈, 가족, 거주, 일에 대한 현실적인 대화를 피하지 않는 것이 좋습니다.

3년 차에는 관계를 이어갈지,
결혼으로 갈 수 있을지 판단이 분명해질 수 있습니다.

✅ 실천 조언

첫째, 설렘보다 안정감을 기준으로 보세요.

둘째, 돈과 생활 습관은 결혼 전에 꼭 확인하세요.

셋째, 서운함을 참지 말고 차분히 말하는 연습이 필요합니다.

넷째, 상대의 말보다 반복되는 행동을 보세요.

다섯째, 결혼은 감정만이 아니라 생활을 함께 만드는 일이라는 점을 기억하세요.

🌙 최종 총평

${name}님의 결혼운은 빠른 결정형보다
천천히 확인하고 안정적으로 쌓아가는 흐름에서 좋아집니다.

좋은 배우자는 ${name}님의 마음을 흔드는 사람이 아니라
삶을 함께 정리하고 지켜줄 수 있는 사람입니다.

결혼을 생각한다면
사랑의 감정뿐 아니라 생활의 현실까지 함께 볼 때
후회 없는 선택을 할 수 있습니다.
`;
}

import type { BasicSajuResult } from "../../types/basic";

export function moneyConsulting(data: BasicSajuResult): string {
  const name = data.name;

  const sajuMoneyPoint =
    `${data.dayMaster} 일간, ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면 ${name}님의 재물운은 단순한 수입보다 돈을 다루는 기준과 관리력에서 더 크게 살아납니다.`;

  const strongMoneyPoint =
    `강하게 살아나는 ${data.strongestElement} 기운은 돈을 벌고 기회를 잡을 때 자연스럽게 드러나는 장점입니다.`;

  const weakMoneyPoint =
    `부족한 ${data.weakestElement} 기운은 돈이 새거나 판단이 흔들릴 수 있는 지점이므로 재물 관리에서 반드시 보완해야 합니다.`;

  return `
🌿 원장님이 먼저 드리는 말씀

제가 ${name}님의 재물 흐름을 볼 때 먼저 말씀드리고 싶은 것은,
돈이 많이 들어오는 것보다 들어온 돈이 얼마나 잘 남는지가 더 중요하다는 점입니다.

${sajuMoneyPoint}

${strongMoneyPoint}

${weakMoneyPoint}

재물운은 단순히 돈을 많이 버느냐만 보는 것이 아닙니다.

돈이 들어오는 방식,
돈이 빠져나가는 습관,
돈을 오래 지키는 기준,
그리고 돈 때문에 마음이 흔들리는 패턴까지 함께 봐야 합니다.

${name}님은 한 번에 크게 잡는 돈보다,
작게 시작해서 꾸준히 쌓아가는 돈의 흐름이 더 안정적인 편입니다.


타고난 재물 성향

${name}님은 돈을 무작정 쓰는 사람이라기보다,
필요하다고 생각하는 곳에는 쓰고,
아깝다고 느끼는 곳에는 쉽게 열지 않는 편입니다.

다만 돈을 쓸 때의 기준이 감정에 따라 달라질 수 있습니다.

그래서 ${name}님에게 가장 중요한 것은 수입보다 기준입니다.

수입이 늘어도 기준이 없으면 돈은 남지 않습니다.
반대로 수입이 아주 크지 않아도 기준이 분명하면 돈은 조금씩 쌓입니다.


돈이 들어오는 방식

${name}님은 한 번에 큰돈이 들어오는 구조보다,
반복적으로 들어오는 돈,
신뢰를 바탕으로 생기는 돈,
경험이 쌓이면서 커지는 돈과 잘 맞습니다.

즉, 단기 투기형 재물보다 누적형 재물이 더 잘 맞습니다.

특히 ${name}님은 한 번 크게 벌자보다
매달 꾸준히 남기자는 방향이 훨씬 안정적입니다.


돈이 새는 패턴

${name}님도 돈이 새는 구멍을 잘 봐야 합니다.

첫째, 기분 때문에 쓰는 돈입니다.

둘째, 체면 때문에 나가는 돈입니다.

셋째, 준비되지 않은 투자입니다.

넷째, 반복 지출입니다.

한 번에 큰돈이 아니어도 매달 빠져나가는 돈이 많으면 재물운은 쉽게 무거워집니다.


앞으로 3년 재물 전략

앞으로 1년은 정리의 시기입니다.
수입을 늘리는 것보다 지출 구조를 다시 보고 돈이 어디로 새는지 확인하는 것이 먼저입니다.

2년 차에는 안정화가 중요합니다.
작게라도 꾸준히 남는 돈을 만들고 6개월, 1년 단위로 돈의 흐름을 보는 습관을 만들어야 합니다.

3년 차에는 확장을 생각해볼 수 있습니다.
다만 무리한 확장보다 검증된 구조 안에서 수입원을 넓히는 것이 좋습니다.


원장님 최종 조언

최종적으로 말씀드리면,
${name}님의 재물운은 크게 한 번 잡는 운보다
작게 시작해서 오래 쌓아가는 운에 가깝습니다.

돈복이 없다는 뜻이 아닙니다.

돈이 들어왔을 때 오래 머물 수 있는 구조를 만들어야 재물운이 살아나는 흐름입니다.

앞으로는 얼마나 버느냐보다
얼마나 남기고, 얼마나 오래 지키느냐를 기준으로 보시면 좋습니다.
`.trim();
}

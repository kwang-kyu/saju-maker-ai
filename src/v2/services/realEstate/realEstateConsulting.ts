import type { BasicSajuResult } from "../../types/basic";

export function getRealEstateConsulting(data: BasicSajuResult): string {
  const name = data.name;

  const sajuRealEstatePoint =
    `${data.dayMaster} 일간, ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면 ${name}님의 부동산운은 감정적인 확신보다 보유 여력, 안정성, 장기 판단에서 더 크게 갈립니다.`;

  const strongRealEstatePoint =
    `강하게 살아나는 ${data.strongestElement} 기운은 부동산 판단에서 자연스럽게 드러나는 장점입니다. 이 기운이 살아날 때 입지, 흐름, 기회를 보는 힘이 좋아집니다.`;

  const weakRealEstatePoint =
    `부족한 ${data.weakestElement} 기운은 부동산에서 보완해야 할 지점입니다. 이 부분이 약하면 매수매도 타이밍이나 대출 판단에서 흔들릴 수 있습니다.`;
  return `
제가 ${name}님의 부동산운을 볼 때 먼저 말씀드리고 싶은 것은,
부동산운은 단순히 집을 사면 좋다 나쁘다가 아니라
언제 움직이고, 무엇을 피하고, 어떤 자산을 오래 가져갈 수 있는지를 보는 것이 중요하다는 점입니다.

${sajuRealEstatePoint}

${strongRealEstatePoint}

${weakRealEstatePoint}

🏡 원장님 말씀

${name}님은 부동산을 볼 때 감정적인 확신보다
입지, 현금흐름, 보유 여력, 장기성을 함께 봐야 합니다.

좋은 물건도 내 상황과 맞지 않으면 부담이 되고,
평범해 보이는 물건도 흐름을 잘 타면 안정적인 자산이 될 수 있습니다.

🏢 부동산 성향

${name}님은 단기 시세차익만 노리는 방식보다는
안정적으로 보유하면서 가치가 쌓이는 부동산이 잘 맞습니다.

특히 실거주 안정,
월세 흐름,
지역 수요,
교통,
생활 인프라처럼 현실적인 요소를 꼼꼼히 봐야 합니다.

💰 매수 판단

매수는 분위기에 휩쓸려 결정하면 안 됩니다.

${name}님은 가격이 오른다는 말보다
내가 감당할 수 있는 대출,
월 부담,
공실 위험,
향후 매도 가능성을 먼저 계산해야 합니다.

좋은 매수는 싸게 사는 것보다
무리하지 않고 오래 버틸 수 있는 구조를 만드는 것입니다.

📉 매도 판단

매도는 손해냐 이익이냐만 보고 판단하면 안 됩니다.

앞으로 더 가져갈 가치가 있는지,
다른 기회로 갈아탈 수 있는지,
세금과 비용을 감안해도 실익이 있는지를 봐야 합니다.

감정적으로 아까워서 못 파는 것도 문제이고,
불안해서 너무 빨리 파는 것도 조심해야 합니다.

⚠️ 투자에서 조심할 점

${name}님은 주변 말만 듣고 급하게 움직이는 투자를 조심해야 합니다.

특히 개발 호재만 강조되는 물건,
수익률만 높게 보이는 물건,
권리관계가 복잡한 물건,
대출 비중이 큰 투자는 신중해야 합니다.

부동산은 들어갈 때보다 나올 때가 더 중요합니다.

🌿 보유 전략

${name}님에게 맞는 부동산은
시간이 지날수록 가치가 유지되거나
임대 수요가 꾸준한 자산입니다.

무리하게 여러 개를 늘리는 것보다
관리 가능한 범위 안에서 좋은 자산을 지키는 것이 좋습니다.

보유 중에는 세금,
수리비,
공실,
금리 변화를 미리 계산해야 합니다.

📌 앞으로 3년 부동산 전략

1년 차에는 무리한 매수보다 시장을 관찰하고
자금 계획을 정리하는 것이 좋습니다.

2년 차에는 실거주와 투자 목적을 분명히 나눠야 합니다.
목적이 섞이면 판단이 흐려질 수 있습니다.

3년 차에는 보유 자산을 점검하고
갈아타기, 정리, 추가 매수 여부를 현실적으로 판단할 수 있습니다.

✅ 실천 조언

첫째, 분위기보다 숫자로 판단하세요.

둘째, 대출은 감당 가능한 범위 안에서만 보세요.

셋째, 호재보다 실제 수요를 먼저 확인하세요.

넷째, 매수 전에는 반드시 매도 가능성까지 생각하세요.

다섯째, 부동산은 오래 버틸 수 있는 사람이 이깁니다.

🌙 최종 총평

${name}님의 부동산운은 급하게 사고파는 흐름보다
신중하게 고르고 안정적으로 보유할 때 좋아집니다.

중요한 것은 좋은 물건을 찾는 것만이 아니라
내가 감당할 수 있는 구조로 가져가는 것입니다.

${name}님은 부동산에서 욕심보다 기준이 중요합니다.

입지, 수요, 현금흐름, 보유 여력을 함께 본다면
부동산은 불안한 투자가 아니라
삶을 안정시키는 자산이 될 수 있습니다.
`;
}

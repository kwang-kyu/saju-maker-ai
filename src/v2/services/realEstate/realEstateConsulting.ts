import type { BasicSajuResult } from "../../types/basic";

export function getRealEstateConsulting(data: BasicSajuResult): string {
  const name = data.name;

  const sajuAssetPoint =
    `${data.dayMaster} 일간과 ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면 ${name}님의 재테크는 단순히 많이 버는 문제보다, 돈이 들어왔을 때 지키고 굴리는 방식에서 성패가 갈립니다.`;

  const strongAssetPoint =
    `강하게 살아나는 ${data.strongestElement} 기운은 ${name}님이 자산을 키울 때 자연스럽게 드러나는 장점입니다. 이 기운이 잘 쓰이면 기회를 보는 눈, 실행력, 판단 기준이 살아납니다.`;

  const weakAssetPoint =
    `부족한 ${data.weakestElement} 기운은 재테크에서 반드시 보완해야 할 부분입니다. 이 부분이 약하면 충동적인 선택, 불안한 보유, 타이밍 착오, 주변 말에 흔들리는 문제가 생길 수 있습니다.`;

  return `
제가 ${name}님의 재테크 흐름을 볼 때 먼저 말씀드리고 싶은 것은,
재테크는 단순히 주식, 부동산, 사업 중 무엇이 좋으냐의 문제가 아니라
${name}님 사주에 맞는 돈의 속도와 감당 가능한 위험을 찾는 과정이라는 점입니다.

${sajuAssetPoint}

${strongAssetPoint}

${weakAssetPoint}

💰 원장님 말씀

${name}님은 돈을 빠르게 불리는 방식보다
돈이 들어오는 길, 새어나가는 습관, 오래 지킬 수 있는 구조를 먼저 봐야 합니다.

수익률만 보고 움직이면 마음이 급해지고,
반대로 너무 보수적으로만 가면 좋은 기회를 놓칠 수 있습니다.

중요한 것은 큰돈을 한 번에 노리는 것이 아니라
내 사주 흐름에 맞는 방식으로 꾸준히 쌓아가는 것입니다.

📌 재테크 성향

${name}님은 감정적으로 확신이 올 때보다
기준을 세우고 숫자로 확인했을 때 재테크 운이 안정됩니다.

특히 현금흐름,
고정지출,
대출 부담,
투자 기간,
회수 가능성을 함께 봐야 합니다.

단기 매매나 주변 추천에 흔들리는 방식보다는
직접 이해할 수 있고 관리 가능한 자산이 더 잘 맞습니다.

🏦 돈을 모으는 방식

${name}님은 돈을 벌기 전보다
돈을 모으는 규칙을 먼저 세워야 합니다.

수입이 늘어도 지출 구조가 흐트러지면 재물이 머물기 어렵습니다.

월 고정저축,
비상금,
투자금,
생활비를 나누고
투자는 남는 돈이 아니라 계획된 돈으로 해야 합니다.

📈 투자 판단

투자를 볼 때는 좋아 보이는 상품보다
${name}님이 버틸 수 있는 기간과 손실 가능성을 먼저 봐야 합니다.

주식, 부동산, 사업, 동업, 금융상품 모두
수익보다 먼저 위험을 계산해야 합니다.

특히 잘 모르는 분야,
남의 말만 듣는 투자,
급하게 결정하는 계약,
대출 비중이 큰 투자는 신중해야 합니다.

🏡 자산 선택 방향

${name}님에게 맞는 재테크는 한 가지로 고정되지 않습니다.

부동산은 안정성과 보유 여력을 따져야 하고,
금융투자는 분산과 기간 설정이 중요하며,
사업 투자는 현금흐름과 손익분기점을 먼저 봐야 합니다.

동업이나 공동투자는 역할, 책임, 돈의 기준이 명확할 때만 검토하는 것이 좋습니다.

⚠️ 조심해야 할 패턴

${name}님은 분위기에 휩쓸려 지금 아니면 안 된다는 식의 선택을 조심해야 합니다.

재테크에서 가장 위험한 순간은
욕심이 커질 때,
불안해서 급하게 움직일 때,
남의 성공 사례를 내 상황에 그대로 적용할 때입니다.

돈은 벌 때보다 지킬 때 실력이 드러납니다.

🌿 보유 전략

${name}님에게 중요한 것은 무리하게 자산을 늘리는 것이 아니라
관리 가능한 범위 안에서 자산의 질을 높이는 것입니다.

현금,
안정형 자산,
성장형 투자,
실물자산의 비중을 나누고
현재 나이와 소득 구조에 맞게 속도를 조절해야 합니다.

📌 앞으로 3년 재테크 전략

1년 차에는 지출 구조와 현금흐름을 정리하는 것이 우선입니다.
무리한 투자보다 내 돈이 어디서 새는지 확인해야 합니다.

2년 차에는 소액으로 경험을 쌓고
나에게 맞는 투자 방식을 선별하는 시기입니다.

3년 차에는 자산을 정리하고
장기 보유할 것과 정리할 것을 나누는 판단이 필요합니다.

✅ 실천 조언

첫째, 수익률보다 손실 가능성을 먼저 보세요.

둘째, 대출과 고정비는 감당 가능한 범위 안에서만 가져가세요.

셋째, 모르는 투자는 절대 크게 시작하지 마세요.

넷째, 부동산, 금융, 사업 투자는 목적을 구분해서 판단하세요.

다섯째, 재테크는 한 번의 대박보다 오래 유지되는 구조가 더 중요합니다.

🌙 최종 총평

${name}님의 재테크 운은 급하게 사고파는 흐름보다
기준을 세우고 안정적으로 쌓아갈 때 좋아집니다.

중요한 것은 어떤 자산이 좋으냐보다
${name}님이 감당할 수 있는 방식으로 돈을 굴리는 것입니다.

${name}님은 욕심보다 기준,
소문보다 숫자,
확신보다 검증이 중요합니다.

이 원칙을 지키면 재테크는 불안한 투자가 아니라
삶을 안정시키는 자산 관리가 될 수 있습니다.
`;
}

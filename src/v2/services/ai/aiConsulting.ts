import type { BasicSajuResult } from "../../types/basic";

export function getAiConsulting(data: BasicSajuResult, concern?: string): string {
  const name = data.name;

  const concernText = concern
    ? `${name}님이 특히 궁금해하신 부분은 "${concern}"입니다. 이 고민을 따로 떼어 보지 않고, 인생 전체 흐름 안에서 함께 풀어보겠습니다.`
    : "지금은 인생 전체 흐름을 중심으로 상담하겠습니다.";

  return `
AI 종합 인생 처방전

${concernText}

한마디로 말하면, ${name}님은 빠르게 뒤집는 사람보다 시간을 두고 자기 기준을 세울 때 운이 살아나는 사람입니다.

${data.dayMaster} 일간과 ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면, ${name}님의 인생은 단순히 운이 좋다 나쁘다로 볼 수 없습니다. 강하게 살아나는 ${data.strongestElement} 기운을 어떻게 살리고, 부족한 ${data.weakestElement} 기운을 어떻게 보완하느냐가 핵심입니다.


원장님이 먼저 보는 핵심

제가 ${name}님의 사주에서 먼저 보는 것은 돈, 직업, 연애, 건강 중 어느 하나만의 문제가 아닙니다.

이 사주는 인생의 방향이 흩어질 때 피로가 커지고, 기준이 잡힐 때 힘이 살아나는 흐름입니다.

그래서 ${name}님에게 가장 중요한 처방은 더 많이 하는 것이 아니라 무엇을 오래 가져갈지 정하는 것입니다.


지금까지 반복된 인생 흐름

${name}님은 겉으로는 괜찮아 보여도 속으로는 책임감과 부담을 많이 안고 지나온 흐름이 있습니다.

남들이 보기에는 버티는 사람처럼 보여도, 실제로는 마음속에서 계속 계산하고 참고 정리해온 시간이 많았을 수 있습니다.

이것은 실패가 아니라 ${name}님이 자기 삶의 기준을 만들어가는 과정입니다.


반드시 살려야 할 강점

${name}님의 가장 큰 강점은 한 번 방향을 잡으면 쉽게 포기하지 않는 힘입니다.

꾸준함, 관찰력, 현실감각, 책임감이 있습니다.

이 장점은 단기간에 크게 드러나는 힘이라기보다 시간이 지나면서 신뢰와 성과로 나타나는 힘입니다.

그래서 ${name}님은 빠르게 튀는 방식보다 경험이 쌓이고, 사람들에게 신뢰가 남고, 내 이름이 기억되는 방향으로 갈 때 운이 좋아집니다.


가장 조심해야 할 약점

가장 조심해야 할 것은 너무 오래 참고 혼자 감당하려는 습관입니다.

괜찮다고 넘기다가 마음속에 피로가 쌓이고, 어느 순간 관계나 일에서 갑자기 지쳐버릴 수 있습니다.

또 조급함 때문에 선택하면 손해가 생기기 쉽습니다.

돈 때문에 사람을 믿거나, 외로움 때문에 관계를 시작하거나, 불안함 때문에 일을 급하게 벌리는 선택은 피해야 합니다.


앞으로 3년 인생 전략

1년 차는 정리의 시기입니다.
생활, 돈, 일, 사람 관계에서 내가 계속 가져갈 것과 줄여야 할 것을 구분해야 합니다.

2년 차는 집중의 시기입니다.
내가 잘하는 일, 오래 가져갈 수 있는 관계, 반복해서 수익이 되는 구조를 분명히 해야 합니다.

3년 차는 확장의 시기입니다.
정리와 집중을 통해 검증된 것만 키워야 합니다. 이때 무리하게 넓히는 것이 아니라 이미 안정된 흐름을 조금씩 키우는 것이 좋습니다.


인생 처방전

첫째, 조급하게 결정하지 마세요.
${name}님은 급하게 움직일수록 손해가 커지고, 기준을 세운 뒤 움직일수록 결과가 좋아집니다.

둘째, 오래 갈 구조를 먼저 만드세요.
돈도, 일도, 관계도 한 번에 크게 얻는 것보다 오래 유지되는 흐름이 더 중요합니다.

셋째, 사람 관계를 정리하세요.
나를 소모시키는 사람과 나를 안정시키는 사람을 구분해야 합니다.

넷째, 건강 리듬을 무시하지 마세요.
몸이 무너지면 판단도 흐려지고, 판단이 흐려지면 돈과 관계도 함께 흔들릴 수 있습니다.

다섯째, 내 이름이 남는 일을 키우세요.
경험이 쌓일수록 신뢰가 커지는 일, 다시 찾는 사람이 생기는 일, 기록으로 남는 일을 키우는 것이 좋습니다.


최종 총평

${name}님의 인생운은 한 번에 크게 뒤집히는 흐름보다 천천히 기준을 세우고 단단하게 쌓아갈 때 좋아집니다.

앞으로의 핵심은 조급함을 줄이고 내 삶에 맞는 선택을 분명히 하는 것입니다.

${name}님은 늦은 사람이 아닙니다. 시간이 갈수록 자기 힘이 살아나는 사람입니다.

지금부터는 더 많이 버티는 삶이 아니라, 오래 가져갈 수 있는 삶을 선택하셔야 합니다.
`.trim();
}

import type { BasicSajuResult } from "../../types/basic";

export function loveConsulting(data: BasicSajuResult): string {
  const name = data.name;

  const sajuLovePoint =
    `${data.dayMaster} 일간, ${data.yearGanZhi}${data.monthGanZhi}${data.dayGanZhi} 흐름을 함께 보면 ${name}님의 연애운은 만남의 횟수보다 관계 안에서 안정감과 신뢰가 어떻게 쌓이는지가 더 중요합니다.`;

  const strongLovePoint =
    `강하게 살아나는 ${data.strongestElement} 기운은 ${name}님이 사랑을 표현하고 관계를 이끌어갈 때 자연스럽게 드러나는 장점입니다.`;

  const weakLovePoint =
    `부족한 ${data.weakestElement} 기운은 연애에서 보완해야 할 지점입니다. 이 부분이 흔들리면 마음은 깊어도 표현이나 선택에서 불안이 생길 수 있습니다.`;
  return `
제가 ${name}님의 연애운을 볼 때 먼저 말씀드리고 싶은 것은,
연애운은 단순히 이성을 만나는 운만 보는 것이 아니라
어떤 사람에게 마음이 움직이고,
어떤 관계에서 편안해지며,
어떤 상황에서 상처를 받기 쉬운지를 함께 봐야 한다는 점입니다.

${sajuLovePoint}

${strongLovePoint}

${weakLovePoint}

💗 원장님 말씀

${name}님은 연애를 가볍게만 흘려보내는 사람은 아닙니다.
마음이 움직이면 상대를 깊이 생각하고,
관계가 안정되기를 바라는 마음이 강한 편입니다.

다만 겉으로 표현하는 방식과 속마음의 깊이가 다를 수 있습니다.
겉으로는 괜찮은 척하거나 담담해 보여도,
속으로는 상대의 말과 행동을 오래 생각하는 경우가 있습니다.

그래서 ${name}님 연애에서는
처음의 설렘보다 시간이 지나면서 쌓이는 신뢰가 더 중요합니다.

💞 연애 성향

${name}님은 상대가 나를 진심으로 존중해준다고 느낄 때
마음을 열기 쉬운 편입니다.

화려한 말이나 순간적인 감정보다는
작은 약속을 지키는 사람,
말과 행동이 크게 다르지 않은 사람,
내 상황을 함부로 판단하지 않는 사람에게 안정감을 느낍니다.

반대로 감정 기복이 심하거나,
말은 많은데 책임감이 부족한 사람과는 오래 가기 어렵습니다.

처음에는 끌릴 수 있어도
시간이 지나면 피로감이 커질 수 있습니다.

❤️ 사랑이 시작되는 방식

${name}님은 갑작스럽게 불타오르는 연애보다
대화를 나누면서 천천히 마음이 열리는 흐름이 좋습니다.

처음부터 강하게 다가오는 사람보다
편안하게 이야기를 들어주고,
부담 없이 곁에 있어주는 사람에게 마음이 움직이기 쉽습니다.

특히 일상적인 대화 속에서
이 사람은 나를 이해하려고 하는구나
라는 느낌을 받을 때 연애운이 살아납니다.

💔 연애에서 반복되기 쉬운 고민

${name}님은 관계가 깊어질수록
상대의 태도 변화에 민감해질 수 있습니다.

처음에는 괜찮다고 넘겼던 말도
시간이 지나면 마음에 남을 수 있고,
서운함을 바로 표현하지 못해 속으로 쌓아두는 경우가 생길 수 있습니다.

문제는 감정을 오래 참고 있다가
어느 순간 갑자기 마음이 닫힐 수 있다는 점입니다.

그래서 ${name}님에게 중요한 것은
서운함을 참는 것이 아니라
상대가 이해할 수 있는 말로 차분히 표현하는 것입니다.

🌿 좋은 인연의 특징

${name}님에게 좋은 인연은
감정을 흔드는 사람보다 생활을 안정시켜주는 사람입니다.

말을 예쁘게 하는 사람보다
꾸준히 행동으로 보여주는 사람이 좋습니다.

또한 ${name}님의 생각과 속도를 존중해주고,
관계를 급하게 몰아가지 않는 사람이 잘 맞습니다.

이런 사람을 만나면 연애가 불안이 아니라
삶의 중심을 잡아주는 힘이 될 수 있습니다.

⚠️ 피해야 할 인연

${name}님은 상대에게 마음을 주면
쉽게 끊어내지 못하는 면이 있을 수 있습니다.

그래서 처음부터 불안한 신호가 반복되는 사람,
약속을 가볍게 여기는 사람,
감정적으로 밀고 당기기를 자주 하는 사람은 조심해야 합니다.

특히 말로는 좋아한다고 하면서
정작 중요한 순간에 책임을 피하는 사람은
${name}님의 마음을 지치게 만들 수 있습니다.

💬 연애가 좋아지는 방법

${name}님은 연애에서 너무 잘하려고 하기보다
편안하게 관계를 유지하는 법을 배우는 것이 중요합니다.

상대의 기분을 지나치게 살피다 보면
정작 내 마음을 놓치기 쉽습니다.

좋은 연애는 한쪽이 계속 맞춰주는 관계가 아니라
서로의 감정과 생활을 함께 조율하는 관계입니다.

그러므로 연애 초반부터
내가 싫은 것,
불편한 것,
원하는 관계의 방향을 조금씩 표현하는 것이 좋습니다.

📌 앞으로 3년 연애 전략

앞으로 3년 동안 ${name}님의 연애운에서 중요한 것은
새로운 만남 자체보다 관계를 고르는 기준입니다.

1년 차에는 사람을 만날 기회가 생기더라도
겉모습이나 분위기만 보고 판단하지 않는 것이 좋습니다.

2년 차에는 실제 생활 습관,
돈을 대하는 방식,
말투,
책임감 같은 현실적인 부분을 봐야 합니다.

3년 차에는 관계를 이어갈지 정리할지에 대한 판단이 분명해질 수 있습니다.
이때 감정보다 현실을 함께 볼 수 있어야 좋은 선택을 할 수 있습니다.

✅ 실천 조언

첫째, 마음에 드는 사람이 생겨도 너무 빨리 확신하지 마세요.
시간을 두고 행동을 보는 것이 좋습니다.

둘째, 서운함을 오래 참지 마세요.
작은 감정일 때 말해야 큰 갈등으로 번지지 않습니다.

셋째, 나를 불안하게 만드는 사람보다
나를 편안하게 만드는 사람을 선택하세요.

넷째, 외로움 때문에 관계를 시작하지 않는 것이 좋습니다.
외로움으로 시작한 관계는 판단이 흐려질 수 있습니다.

다섯째, 연애에서도 내 생활 리듬을 지키세요.
내 삶이 무너지지 않아야 관계도 건강해집니다.

🌙 최종 총평

${name}님의 연애운은 가볍고 빠른 인연보다
천천히 신뢰를 쌓아가는 인연에서 좋아집니다.

중요한 것은 많이 만나는 것이 아니라
나를 안정시키는 사람을 알아보는 눈입니다.

${name}님은 사랑을 하면 깊이 마음을 주는 사람이기 때문에
상대 선택이 무엇보다 중요합니다.

앞으로의 연애에서는 설렘만 보지 말고
이 사람이 내 마음을 편안하게 해주는 사람인지,
내 삶을 함께 건강하게 만들어갈 수 있는 사람인지를 보셔야 합니다.
`;
}
